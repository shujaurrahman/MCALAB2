
import java.util.Scanner;
import java.lang.Math;
public class week3que2 {
    public static void main(String[] args) {
        System.out.println("Program to Print the Cubes and Squares for the Numbers 1-5. ");
        System.out.println("\n Square of the Numbers: ");
        for(int i=1;i<=5;i++){
            System.out.println("Square of "+i+" is : "+i*i);
        }
        System.out.println("\n Cubes of the Numbers: ");
        for(int i=1;i<=5;i++){
            System.out.println("Cubes of "+i+" is : "+i*i*i);
        }

    }
}
